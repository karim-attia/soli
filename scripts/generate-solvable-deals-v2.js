#!/usr/bin/env node

const { spawn } = require('node:child_process')
const fs = require('node:fs')
const os = require('node:os')
const path = require('node:path')

const ROOT = path.resolve(__dirname, '..')
const OUTPUT_PATH = path.join(ROOT, 'src/data/solvableDealsV2.generated.ts')
const DEFAULT_LONELYBOT_DIR = path.resolve(ROOT, '../Lonelybot/lonelybot')

// Offline solvable-catalog generator. Lonelybot checks app-compatible exact deals
// and writes `exactId:drawMask` rows; the app later only selects from this catalog.
const parseArgs = (argv) => {
  const args = new Map()
  for (let index = 2; index < argv.length; index += 1) {
    const item = argv[index]
    if (!item.startsWith('--')) {
      continue
    }
    const [key, value] = item.slice(2).split('=')
    args.set(key, value ?? 'true')
  }
  return args
}

const readPositiveInteger = (args, key, fallback) => {
  const value = Number(args.get(key) ?? fallback)
  if (!Number.isInteger(value) || value <= 0) {
    throw new Error(`--${key} must be a positive integer.`)
  }
  return value
}

const readNonNegativeInteger = (args, key, fallback) => {
  const value = Number(args.get(key) ?? fallback)
  if (!Number.isInteger(value) || value < 0) {
    throw new Error(`--${key} must be a non-negative integer.`)
  }
  return value
}

const runCommand = (command, commandArgs, options) =>
  new Promise((resolve, reject) => {
    const child = spawn(command, commandArgs, {
      cwd: options.cwd,
      env: process.env,
      stdio: ['ignore', 'pipe', 'pipe'],
    })

    const prefixLines = (chunk, target) => {
      chunk
        .toString()
        .split(/\r?\n/)
        .filter(Boolean)
        .forEach((line) => target.write(`${options.prefix}${line}\n`))
    }

    child.stdout.on('data', (chunk) => prefixLines(chunk, process.stdout))
    child.stderr.on('data', (chunk) => prefixLines(chunk, process.stderr))
    child.on('error', reject)
    child.on('close', (code) => {
      if (code === 0) {
        resolve()
        return
      }
      reject(new Error(`${command} ${commandArgs.join(' ')} exited with code ${code}`))
    })
  })

const splitRanges = (total, startSeed, chunks) => {
  const baseSize = Math.floor(total / chunks)
  const remainder = total % chunks
  let cursor = startSeed

  return Array.from({ length: chunks }, (_, index) => {
    const size = baseSize + (index < remainder ? 1 : 0)
    const range = { startSeed: cursor, total: size, index }
    cursor += size
    return range
  }).filter((range) => range.total > 0)
}

const parseRows = (filePath) => {
  const source = fs.readFileSync(filePath, 'utf8')
  return [...source.matchAll(/'(E1_[0-9a-z]+:\d+)'/gi)].map((match) => match[1])
}

const renderOutput = (
  rows,
  options
) => `// Generated by scripts/generate-solvable-deals-v2.js.
// Source: Lonelybot harvest-v2 over ${options.total.toLocaleString('en-US')} default seeds, Draw 1-5.
// Rows are exact app deal IDs with a draw-count bit mask.

export const SOLVABLE_DEALS_V2 = [
${rows.map((row) => `  '${row}',`).join('\n')}
] as const
`

const main = async () => {
  const args = parseArgs(process.argv)
  const total = readPositiveInteger(args, 'total', 50000)
  const startSeed = readNonNegativeInteger(args, 'start-seed', 0)
  const chunks = readPositiveInteger(args, 'chunks', 5)
  const reportEvery = readPositiveInteger(args, 'report-every', 1000)
  const timeoutMs = readPositiveInteger(args, 'timeout-ms', 60000)
  const lonelybotDir = path.resolve(args.get('lonelybot-dir') ?? DEFAULT_LONELYBOT_DIR)
  const outputPath = path.resolve(args.get('out') ?? OUTPUT_PATH)
  const skipBuild = args.get('skip-build') === 'true'
  const tempDir = fs.mkdtempSync(path.join(os.tmpdir(), 'soli-solvable-v2-'))

  if (!fs.existsSync(path.join(lonelybotDir, 'Cargo.toml'))) {
    throw new Error(`Lonelybot Cargo.toml not found at ${lonelybotDir}`)
  }

  if (!skipBuild) {
    await runCommand('cargo', ['build', '--release', '-p', 'lonecli'], {
      cwd: lonelybotDir,
      prefix: '[lonelybot build] ',
    })
  }

  const ranges = splitRanges(total, startSeed, Math.min(chunks, total))
  await Promise.all(
    ranges.map((range) => {
      const partPath = path.join(tempDir, `part-${range.index}.ts`)
      return runCommand(
        './target/release/lonecli',
        [
          'harvest-v2',
          '--total',
          String(range.total),
          '--start-seed',
          String(range.startSeed),
          '--out',
          partPath,
          '--timeout-ms',
          String(timeoutMs),
          '--report-every',
          String(reportEvery),
        ],
        {
          cwd: lonelybotDir,
          prefix: `[harvest ${range.index}] `,
        }
      )
    })
  )

  const rows = Array.from(
    new Set(
      ranges.flatMap((range) => parseRows(path.join(tempDir, `part-${range.index}.ts`)))
    )
  ).sort()

  fs.writeFileSync(outputPath, renderOutput(rows, { total }))
  process.stdout.write(`Generated ${rows.length} v2 solvable deals at ${outputPath}\n`)
}

main().catch((error) => {
  console.error(error)
  process.exit(1)
})
